int	ft_strcmp(char *s1, char *s2)
{
	char	*ps1;
	char	*ps2;

	ps1 = s1;
	ps2 = s2;
	while (*ps1 && *ps1 == *ps2)
	{
		++ps1;
		++ps2;
	}
	return (*ps1 - *ps2);
}
