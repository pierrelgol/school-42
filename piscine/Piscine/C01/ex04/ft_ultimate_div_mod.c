void	ft_ultimate_div_mod(int *a, int *b)
{
	int	vb;
	int	va;

	if (a != (void *)0 && b != (void *)0 && *b != 0)
	{
		va = *a;
		vb = *b;
		if (vb < 0)
		{
			*a = -1;
			vb *= *a;
		}
		else
			*a = 1;
		*a *= (va / vb);
		*b = va - (*a * vb);
	}
}
