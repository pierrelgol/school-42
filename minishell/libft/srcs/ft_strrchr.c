#include "../includes/libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	i;
	int	pos;

	i = 0;
	pos = -1;
	while (s[i] != '\0')
	{
		if (s[i] == (unsigned char)c)
			pos = i;
		i++;
	}
	if (s[i] == (unsigned char)c)
		return ((char *)&s[i]);
	else if (pos >= 0)
		return ((char *)&s[pos]);
	return (NULL);
}
